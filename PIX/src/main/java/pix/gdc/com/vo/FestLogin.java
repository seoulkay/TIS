package pix.gdc.com.vo;

public class FestLogin {
	Integer idx = 0;
	String id;
	String password;
	String fest_event_number;
	String official_name;
	
	
	public Integer getIdx() {
		return idx;
	}
	public void setIdx(Integer idx) {
		this.idx = idx;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getFest_event_number() {
		return fest_event_number;
	}
	public void setFest_event_number(String fest_event_number) {
		this.fest_event_number = fest_event_number;
	}
	public String getOfficial_name() {
		return official_name;
	}
	public void setOfficial_name(String official_name) {
		this.official_name = official_name;
	}
	
	
	
	

}
