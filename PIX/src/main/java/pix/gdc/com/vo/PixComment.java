package pix.gdc.com.vo;

public class PixComment {
	private int pix_comment_idx;
	private String pix_comment;
	private int pix_idx;
	private String is_use;
	
	public int getPix_comment_idx() {
		return pix_comment_idx;
	}
	public void setPix_comment_idx(int pix_comment_idx) {
		this.pix_comment_idx = pix_comment_idx;
	}
	public String getPix_comment() {
		return pix_comment;
	}
	public void setPix_comment(String pix_comment) {
		this.pix_comment = pix_comment;
	}
	public int getPix_idx() {
		return pix_idx;
	}
	public void setPix_idx(int pix_idx) {
		this.pix_idx = pix_idx;
	}
	public String getIs_use() {
		return is_use;
	}
	public void setIs_use(String is_use) {
		this.is_use = is_use;
	}
	
	

}
